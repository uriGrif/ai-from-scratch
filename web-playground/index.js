function relu(x) {
	return x.map(i => Math.max(0, i));
}

function softmax(x) {
	let sum = 0;
	x.forEach(i => {
		sum += Math.exp(i);
	});
	return x.map(i => Math.exp(i) / sum);
}

function multiply(a, b) {
	var aNumRows = a.length,
		aNumCols = a[0].length,
		bNumRows = b.length,
		bNumCols = b[0].length,
		m = new Array(aNumRows); // initialize array of rows

	// console.log(aNumRows);
	// console.log(aNumCols);
	// console.log(bNumRows);
	// console.log(bNumCols);
	for (var r = 0; r < aNumRows; ++r) {
		m[r] = new Array(bNumCols); // initialize the current row
		for (var c = 0; c < bNumCols; ++c) {
			m[r][c] = 0; // initialize the current cell
			for (var i = 0; i < aNumCols; ++i) {
				m[r][c] += a[r][i] * b[i][c];
			}
		}
	}
	return m;
}

function findMaxIndex(arr) {
	let max = 0;
	let maxIndex;
	arr.forEach((e, i) => {
		if (max < e) {
			max = e;
			maxIndex = i;
		}
	});
	return maxIndex;
}

function predict() {
	// console.log(paintedList);
	let output = paintedList;
	let deactivated = [];
	for (let i = 0; i < myModel.length; i++) {
		output[0].push(1);
		deactivated = multiply(output, myModel[i].weights);
		if (myModel[i].activationType === 1) {
			output[0] = relu(deactivated[0]);
		} else if (myModel[i].activationType === 2) {
			output[0] = softmax(deactivated[0]);
		}
	}
	// console.log(output);
	let pred = document.getElementById("prediction");
	pred.innerHTML = `Prediction: ${findMaxIndex(output[0])}`;
	output[0].forEach((e, i) => {
		let prob = document.getElementById(`prob-${i}`);
		prob.innerHTML = Math.round(e * 100) / 100;
	});
}
